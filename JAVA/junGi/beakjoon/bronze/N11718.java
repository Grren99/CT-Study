package JAVA.junGi.beakjoon.bronze;

import java.util.Scanner;

public class N11718 {
    // 입력한 그대로 출력하기
    public static void main(String[] args){
        Scanner sc = new Scanner(System.in);
        // sc 입력된 값이 있다면 계속 출력
        while(sc.hasNext()){
            System.out.println(sc.nextLine());
        }
    }
}
